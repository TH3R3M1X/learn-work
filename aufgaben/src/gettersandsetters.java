public class gettersandsetters {
    class Complex {

        private double real;
        private double image;

        public double getReal(){
            return real;
        }
        public double getImage(){
            return image;
        }
        public void setReal(double newReal){
            this.real = newReal;
        }
        public void setImage(double newImage){
            this.image = newImage;
        }
    }

}
